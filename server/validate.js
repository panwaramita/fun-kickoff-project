const { check, validationResult } = require("express-validator");

exports.validateRegister = [
  check("username", "Please enter a username").not().isEmpty(),
  check("email", "Please enter a valid email address").isEmail(),
  check(
    "password",
    "Please enter a password with 6 or more characters"
  ).isLength({
    min: 6
  }),
  (req, res, next) => {
    const errors = validationResult(req);

    console.log(errors);
    if (!errors.isEmpty())
      return res.status(400).json({ errors: errors.array() });
    next();
  }
];

exports.validateLogin = [
  check("email", "Please enter a valid email address").isEmail(),
  check("password", "Password is required").not().isEmpty(),
  (req, res, next) => {
    const errors = validationResult(req);

    if (!errors.isEmpty())
      return res.status(400).json({ errors: errors.array() });
    next();
  }
];

exports.validateProfile = [
  check("firstName", "Please enter a firstname").not().isEmpty(),
  check("lastName", "Please enter a lastname").not().isEmpty(),
  check("description", "Please enter a description").not().isEmpty(),
  check("phoneNo.", "Please enter a PhoneNo.").not().isEmpty(),
  check("photoUrl", "Please enter a photoUrl").not().isEmpty(),
  check("city", "Please enter a city").not().isEmpty(),
  check("dateOfBirth", "Please enter a Date of Birth").not().isEmpty().isDate(),
  (req, res, next) => {
    const errors = validationResult(req);

    console.log(errors);
    if (!errors.isEmpty())
      return res.status(400).json({ errors: errors.array() });
    next();
  }
];